sname = 'case_3_proper_zoh_fs10';
fprintf(fid, 'struct %s\n{\n', sname);
fprintf(fid, '    // H(s) = (s+2)/(s+1), ZOH, fs=10 Hz\n');
fprintf(fid, '    static constexpr double b_s[2] = {%.17g, %.17g};\n', b_s(1), b_s(2));
fprintf(fid, '    static constexpr double a_s[2] = {%.17g, %.17g};\n', a_s(1), a_s(2));
fprintf(fid, '    static constexpr double sample_rate_hz = %.1f;\n\n', fs);
fprintf(fid, '    static constexpr double b[%d] = {', length(b_d));
for i = 1:length(b_d)
    if i > 1; fprintf(fid, ', '); end
    fprintf(fid, '%.17g', b_d(i));
end
fprintf(fid, '};\n');
fprintf(fid, '    static constexpr double a[%d] = {', length(a_d));
for i = 1:length(a_d)
    if i > 1; fprintf(fid, ', '); end
    fprintf(fid, '%.17g', a_d(i));
end
fprintf(fid, '};\n');
fprintf(fid, '    static constexpr double step[%d] = {', STEP_LEN);
for i = 1:STEP_LEN
    if i > 1; fprintf(fid, ', '); end
    fprintf(fid, '%.17g', y(i));
end
fprintf(fid, '};\n');
fprintf(fid, '};\n\n');

% ---------------------------------------------------------------------------
% Case 4: H(s) = 1/(s^2 + 3s + 2), MatchedZ, fs=10 Hz
% ---------------------------------------------------------------------------
b_s = [0, 0, 1];
a_s = [1, 3, 2];
fs  = 10.0;
Ts  = 1.0 / fs;
sys_c = tf(b_s, a_s);
sys_mz = c2d(sys_c, Ts, 'matched');
[b_mz, a_mz] = tfdata(sys_mz, 'v');
while length(b_mz) < length(a_mz); b_mz = [0, b_mz]; end
y = filter(b_mz, a_mz, u);

sname = 'case_4_mz_fs10';
fprintf(fid, 'struct %s\n{\n', sname);
fprintf(fid, '    // H(s) = 1/(s^2+3s+2), MatchedZ, fs=10 Hz\n');
fprintf(fid, '    static constexpr double b_s[3] = {%.17g, %.17g, %.17g};\n', b_s(1), b_s(2), b_s(3));
fprintf(fid, '    static constexpr double a_s[3] = {%.17g, %.17g, %.17g};\n', a_s(1), a_s(2), a_s(3));
fprintf(fid, '    static constexpr double sample_rate_hz = %.1f;\n\n', fs);
fprintf(fid, '    static constexpr double b[%d] = {', length(b_mz));
for i = 1:length(b_mz)
    if i > 1; fprintf(fid, ', '); end
    fprintf(fid, '%.17g', b_mz(i));
end
fprintf(fid, '};\n');
fprintf(fid, '    static constexpr double a[%d] = {', length(a_mz));
for i = 1:length(a_mz)
    if i > 1; fprintf(fid, ', '); end
    fprintf(fid, '%.17g', a_mz(i));
end
fprintf(fid, '};\n');
fprintf(fid, '    static constexpr double step[%d] = {', STEP_LEN);
for i = 1:STEP_LEN
    if i > 1; fprintf(fid, ', '); end
    fprintf(fid, '%.17g', y(i));
end
fprintf(fid, '};\n');
fprintf(fid, '};\n\n');

fprintf(fid, '} // namespace ctf_ref\n\n');
fprintf(fid, '#endif // CONSTFILT_CONTINUOUS_TF_REFERENCE_HPP\n');
fclose(fid);

fprintf('Written: %s\n', outfile);
